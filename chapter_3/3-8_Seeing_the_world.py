#list with the name of the places that i want to visit

places = ["punta cana", "barcelona", "tokyo", "pekin"]
print("Esta es la lista")
print(places)

print("esta es la organizada alfabeticamente, pero temporalmente")
print(sorted(places))

print("imprimir la lista para ver no hubo cambios permanentes")
print(places)

print("esta es la lista invertida")
places.reverse()
print(places)

print("esta es lista invertida nuevamente")
places.reverse()
print(places)

print("esta es la lista organizda alfabeticamente")
places.sort()
print([places])

print("esta es la lista revirtiendo el orden alfabeticamente")
places.sort(reverse=True)
print(places)

